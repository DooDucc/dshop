import { createAsyncThunk } from "@reduxjs/toolkit";
import brandService from "./services";

export const getBrands = createAsyncThunk(
  "brand/getBrands",
  async (thunkAPI) => {
    try {
      return await brandService.getBrands();
    } catch (error) {
      return thunkAPI.rejectWithValue(error);
    }
  }
);

